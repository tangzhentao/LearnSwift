import Foundation

/*
 字符串字面值存放在代码段的常量区
 变量str1中存放的就是这段字符串，因为长度没有超过15
 
 str1存放的数据为：0x3736353433323130 0xe800000000000000
 第一个8字节存放的是字符串中字符的ASCII码
 第二个8字节中的后7字节也用来存储字符中的字符的ASCII码
 第二个8字节中的第1个字节中的前4位存放字符串变量的类型，这里是e
 第二个8字节中的第1个字节中的后4位存放字符串的长度，这里是8，最大值是f也即是存放15个字符
 */
